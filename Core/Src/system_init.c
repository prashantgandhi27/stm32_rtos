/**
  * @brief  print on serial terminal.
  * @retval None
  */
void DebugPrint(UART_HandleTypeDef *uart, char pData[])
{
	HAL_UART_Transmit(uart, (uint8_t *)pData, strlen(pData), 10);
	char newLine[2] = "\r\n";
	HAL_UART_Transmit(uart, (uint8_t *)newLine, strlen(newLine), 10);
}

